# MANUAL_SETUP.ps1 - Ручная настройка в интерактивном режиме
# Копируйте и вставляйте команды по одной

# ============================================================
# ШАГ 1: Разрешить выполнение скриптов
# ============================================================
Set-ExecutionPolicy RemoteSigned -Scope CurrentUser -Force

# ============================================================
# ШАГ 2: Установить Chocolatey (пакетный менеджер)
# ============================================================
Set-ExecutionPolicy Bypass -Scope Process -Force
[System.Net.ServicePointManager]::SecurityProtocol = [System.Net.ServicePointManager]::SecurityProtocol -bor 3072
iex ((New-Object System.Net.WebClient).DownloadString('https://community.chocolatey.org/install.ps1'))

# ============================================================
# ШАГ 3: Установить Node.js
# ============================================================
choco install nodejs-lts -y

# ============================================================
# ШАГ 4: Установить Python
# ============================================================
choco install python -y

# ============================================================
# ШАГ 5: Установить PostgreSQL
# ============================================================
choco install postgresql16 -y

# ============================================================
# ШАГ 6: Установить Git
# ============================================================
choco install git -y

# ============================================================
# ШАГ 7: Перезапустить PowerShell (закройте и откройте заново)
# ============================================================

# ============================================================
# ШАГ 8: Запустить PostgreSQL
# ============================================================
Start-Service postgresql*-x64-16

# ============================================================
# ШАГ 9: Создать базу данных
# ============================================================
$env:PGPASSWORD = "postgres"
& "C:\Program Files\PostgreSQL\16\bin\createdb.exe" -U postgres ktru

# ============================================================
# ШАГ 10: Клонировать репозиторий
# ============================================================
cd C:\
New-Item -ItemType Directory -Path Projects -Force
cd Projects
git clone https://github.com/Den-Snaker/Normalizer.git
cd Normalizer

# ============================================================
# ШАГ 11: Установить зависимости Frontend
# ============================================================
cd frontend
npm install
cd ..

# ============================================================
# ШАГ 12: Установить зависимости Backend
# ============================================================
cd backend
python -m pip install --upgrade pip
pip install -r requirements.txt
cd ..

# ============================================================
# ШАГ 13: Создать .env.local для Frontend
# ============================================================
$envContent = @"
# Google Gemini API Key
VITE_GOOGLE_API_KEY=ВАШ_GOOGLE_API_KEY

# OpenRouter API Key
VITE_OPENROUTER_API_KEY=ВАШ_OPENROUTER_API_KEY

# Backend URL
VITE_API_URL=http://localhost:8000
"@
Set-Content -Path "frontend\.env.local" -Value $envContent -Encoding UTF8

# ============================================================
# ШАГ 14: Создать .env для Backend
# ============================================================
$envContent = "DATABASE_URL=postgresql://postgres:postgres@localhost:5432/ktru"
Set-Content -Path "backend\.env" -Value $envContent -Encoding UTF8

# ============================================================
# ШАГ 15: Создать таблицы в базе данных
# ============================================================
cd backend
python -c "from database import engine; from main import Base; Base.metadata.create_all(bind=engine)"
cd ..

# ============================================================
# ШАГ 16: Запуск Frontend (в одном терминале)
# ============================================================
cd frontend
npm run dev

# ============================================================
# ШАГ 17: Запуск Backend (в другом терминале)
# ============================================================
cd backend
python -m uvicorn main:app --host 0.0.0.0 --port 8000 --reload

# ============================================================
# ГОТОВО! Откройте в браузере: http://localhost:3000
# ============================================================