# MANUAL_BACKEND_INSTALL.ps1 - Ручная установка backend зависимостей
# Копируйте и выполняйте команды по одной

# ============================================================
# ШАГ 1: Проверка Python
# ============================================================
python --version
# Должно показать: Python 3.11.x или выше

# ============================================================
# ШАГ 2: Обновление pip
# ============================================================
python -m pip install --upgrade pip

# ============================================================
# ШАГ 3: Переход в папку backend
# ============================================================
cd backend

# ============================================================
# ШАГ 4: Установка зависимостей по одной
# ============================================================

# FastAPI
python -m pip install fastapi

# Uvicorn с расширениями
python -m pip install uvicorn[standard]

# SQLAlchemy
python -m pip install sqlalchemy

# Psycopg2
python -m pip install psycopg2-binary

# Pydantic
python -m pip install pydantic

# Python-multipart
python -m pip install python-multipart

# Все остальные зависимости из requirements.txt
python -m pip install -r requirements.txt

# ============================================================
# ШАГ 5: Проверка установки
# ============================================================
python -m pip list

# ============================================================
# ШАГ 6: Возврат в корень проекта
# ============================================================
cd ..

# ============================================================
# ШАГ 7: Запуск backend (если frontend уже запущен)
# ============================================================
cd backend
python -m uvicorn main:app --host 0.0.0.0 --port 8000 --reload

